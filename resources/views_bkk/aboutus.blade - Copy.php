            There is a big buzz about the On-demand economy. The fact that you can order an uber and it comes right to you. And maybe this principal can also be applied to people. That when you want an accountant you can find the closest one to you. Or that if distance isn't a problem you can hire your team in another part of the world where you pay a fraction of the price.

            As with any technology though mindGigs is run by people. We are simply enabling you to collect payments, pay and find each other. So this will mean that sometimes you will have amazing people experiences and other times you won't have pleasant experiences.

            Learn to do what is known as an affordable loss. Take someone for an hour. It doesn't work.

            We aim to be fair. So if your service provider doesn't turn up then you can request that we don't pay them. But if they do turn up and provide the service which you are not happy with than you can leave a poor review. If the provider provides a money back guarantee than you can even get your money back.

